const SUBTITLES = ['修订版', '未删节', '长篇', '完整版，大概', '作者恳请再版', '第二版，第一版印错了', '豪华版，送书签']

export function getBaseTitle(title: string): string {
  // Strip subtitle after last " · "
  const dotIdx = title.lastIndexOf(' · ')
  if (dotIdx > 0) {
    const suffix = title.slice(dotIdx + 3)
    if (SUBTITLES.includes(suffix) || /\d+版/.test(suffix) || /[^\s]{2,}/.test(suffix)) {
      return title.slice(0, dotIdx)
    }
  }
  return title
}

export function titleToSlug(title: string): string {
  const base = getBaseTitle(title)
  return base
    .replace(/[：:]/g, '-')
    .replace(/[？?！!。，,、（）()【】\[\]《》""·]/g, '')
    .replace(/\s+/g, '-')
    .replace(/\/+/g, '-')
    .trim()
}
